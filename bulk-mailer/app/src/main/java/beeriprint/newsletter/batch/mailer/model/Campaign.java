package beeriprint.newsletter.batch.mailer.model;

import java.util.Date;

/**
 *
 * @author hilel
 */
public class Campaign {

    private long id;
    private String templateName;
    private String listFile;
    private Date campaignDate;

    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the templateName
     */
    public String getTemplateName() {
        return templateName;
    }

    /**
     * @param templateName the templateName to set
     */
    public void setTemplateName(String templateName) {
        this.templateName = templateName;
    }

    /**
     * @return the listFile
     */
    public String getListFile() {
        return listFile;
    }

    /**
     * @param listFile the listFile to set
     */
    public void setListFile(String listFile) {
        this.listFile = listFile;
    }

    /**
     * @return the campaignDate
     */
    public Date getCampaignDate() {
        return campaignDate;
    }

    /**
     * @param campaignDate the campaignDate to set
     */
    public void setCampaignDate(Date campaignDate) {
        this.campaignDate = campaignDate;
    }

}
